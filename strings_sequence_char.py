# Sequence Characters in Python

# "\n" Newline (Line Break)
print("Hello everyone ,\nI am Aashish.")

# "/t" Tab Space
print("Aashish\tKumar")  # "Aashish Kumar"

# "\b"	Backspace (Removes previous character)
print("Aashishx\b Kumar")	# remove 'x' an print 'Aashish Kumar'

# "\f"	Form Feed (Page Break)
print("Aashish\fKumar")	  # Aashish‚êäKumar

# (\')	Single Quote
print('Aashish\'s mac')	  # Aashish's mac

# (\")	Double Quote
print("My self \"Aashish\"")	 # My self "Aashish"

# (\\)	Backslash
print("C:\\Users\\Aashish")	 # C:\Users\Aashish