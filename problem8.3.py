# How do you prevent a python print() function to print a new line at the end.

# answer is by writting (end="") in end of print function

print("hii")            # automatically goes to next line
print("Aash",end="")        # not goes to next line
print("ish\n",end="")        # not goes to next line it goes bu \n
print("hello\nguys.")   # \n for move to next line between hello and guys